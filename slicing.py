#slicing- get a sublist -part of the list
#get multiple elements
#var[start:stop:step]
#output: start element to stop-1
l=[4,8,2,34,56,4,7,8,5,42]
print(l[0:5]) #index 0to index 4
print(l[2:]) #index 2 to entire
print(l[:]) #index entire
print(l[-4:-1]) #index -4 to index -2
print(l[1:8:2]) #index 1 to 7 with ski[p 1 character 
print(l[::-1]) #reversing entire string


